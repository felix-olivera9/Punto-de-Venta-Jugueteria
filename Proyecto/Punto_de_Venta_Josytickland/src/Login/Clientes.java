    private void cargarProductos() {
    DefaultTableModel modelo = new DefaultTableModel();
    modelo.addColumn("ID");
    modelo.addColumn("Nombre del producto");
    modelo.addColumn("Estado");
    modelo.addColumn("Precio");
    modelo.addColumn("Stock");

    ResultSet rs = ConexionMySQL.listarTodo("producto");

    try {
        while (rs != null && rs.next()) {
            modelo.addRow(new Object[]{
                rs.getInt("id"),
                rs.getString("nombre"),
                rs.getString("estado"),
                rs.getString("precio"),
                rs.getInt("stock")
                
            });
        }
        tblLista.setModel(modelo); // Aseg√∫rate de que TablaDatos sea tu JTable
    } catch (SQLException e) {
        JOptionPane.showMessageDialog(this, "Error al cargar usuarios: " + e.getMessage());
    }
}
    
    
    
    public void buscarProductoPorNombre() {
    String texto = txtBusc.getText().trim();

    if (texto.isEmpty()) {
        JOptionPane.showMessageDialog(null, "Ingresa el nombre del producto a buscar.");
        return;
    }

    DefaultTableModel modelo = new DefaultTableModel();
    modelo.addColumn("ID");
    modelo.addColumn("Nombre del producto");
    modelo.addColumn("Estado");
    modelo.addColumn("Precio");
    modelo.addColumn("Stock");

    // Usamos LOWER para ignorar may√∫sculas/min√∫sculas
    String sql = "SELECT * FROM producto WHERE LOWER(nombre) LIKE ?";

    try {
        Connection con = conectar();
        PreparedStatement ps = con.prepareStatement(sql);
        ps.setString(1, "%" + texto.toLowerCase() + "%");

        ResultSet rs = ps.executeQuery();

        boolean encontrado = false;

        while (rs.next()) {
            Object[] fila = new Object[]{
                rs.getInt("id"),
                rs.getString("nombre"),
                rs.getString("estado"),
                rs.getDouble("precio"),
                rs.getInt("stock")
            };
            modelo.addRow(fila);
            encontrado = true;
        }

        tblLista.setModel(modelo);

        if (!encontrado) {
            JOptionPane.showMessageDialog(null, "No se encontr√≥ ning√∫n producto con ese nombre.");
        }

        rs.close();
        ps.close();
        con.close();

    } catch (SQLException e) {
        JOptionPane.showMessageDialog(null, "Error al buscar producto: " + e.getMessage());
    }
}

