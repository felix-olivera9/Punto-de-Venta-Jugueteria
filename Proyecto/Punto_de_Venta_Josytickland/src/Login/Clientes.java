    private void btnAgregarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnAgregarActionPerformed
        // TODO add your handling code here:
        int fila = tblLista.getSelectedRow();
        if (fila == -1) {
            JOptionPane.showMessageDialog(null, "Selecciona un producto primero.");
            return;
        }

        // Obtener los datos seleccionados
        int id = (int) tblLista.getValueAt(fila, 0);
        String nombre = (String) tblLista.getValueAt(fila, 1);
        double precio = (double) tblLista.getValueAt(fila, 3); // columna precio
        int stock = (int) tblLista.getValueAt(fila, 4);         // columna stock

        // Puedes pedir al usuario la cantidad
        String cantidadStr = JOptionPane.showInputDialog("Cantidad a agregar:");
        if (cantidadStr == null || cantidadStr.trim().isEmpty()) return;

        int cantidad = Integer.parseInt(cantidadStr);

        if (cantidad <= 0 || cantidad > stock) {
            JOptionPane.showMessageDialog(null, "Cantidad inv√°lida o supera el stock.");
            return;
        }

        // Agregar al modelo del carrito
        productoenlist p = new productoenlist(id, nombre, cantidad, precio);
        VerCarrito.modeloCarrito.addRow(p.toObjectArray());

        JOptionPane.showMessageDialog(null, "Producto agregado al carrito.");
    
    }//GEN-LAST:event_btnAgregarActionPerformed

