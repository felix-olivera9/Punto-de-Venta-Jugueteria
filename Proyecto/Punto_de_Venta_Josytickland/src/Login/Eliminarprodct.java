    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed
        // TODO add your handling code here:
        if(tCant.getText().equals("")){

            lblM.setText("Especifique una cantidad.");
            lblM.setForeground(Color.red);

        }else{
            String nombreProducto = cmbP.getSelectedItem().toString();
            int cantidad=Integer.parseInt(tCant.getText());
            
           try {
        // 1. Obtener ID y stock actual del producto
        String consulta = "SELECT id, stock FROM producto WHERE nombre = ?";
        Connection con = conectar();
        PreparedStatement ps = con.prepareStatement(consulta);
        ps.setString(1, nombreProducto);
        ResultSet rs = ps.executeQuery();

        if (rs.next()) {
            int idProducto = rs.getInt("id");
            int stockActual = rs.getInt("stock");

            // Validar que hay suficiente stock
            if (cantidad > stockActual) {
                JOptionPane.showMessageDialog(null, "La cantidad a eliminar excede el stock actual.");
                return;
            }

            int nuevoStock = stockActual - cantidad;

            // 2. Crear mapa con datos a actualizar
            Map<String, Object> datos = new HashMap<>();
            datos.put("stock", nuevoStock);

            // 3. Si el nuevo stock es 0, cambiar el estado a "No disponible"
            if (nuevoStock == 0) {
                datos.put("estado", "No disponible");
            }

            // 4. Ejecutar actualizaci√≥n
            ConexionMySQL.actualizar("producto", datos, "id = " + idProducto);

            
        } else {
            JOptionPane.showMessageDialog(null, "Producto no encontrado.");
        }

        rs.close();
        ps.close();
        con.close();

    } catch (SQLException e) {
        JOptionPane.showMessageDialog(null, "Error al actualizar stock: " + e.getMessage());
    }

            lblM.setText("Se eliminaron correctamente los productos.");
            lblM.setForeground(Color.black);

        }
    }//GEN-LAST:event_jButton2ActionPerformed

